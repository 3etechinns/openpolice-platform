<h1 class="article__header">Privacy</h1>
<h2>Protection des données à caractère personnel</h2>
<p>La police respecte la vie privée des utilisateurs. Bien que la plupart des informations
    soient disponibles sur ce site sans devoir fournir des données à caractère personnel, il est possible que des
    informations personnelles soient demandées à l'utilisateur.</p>
<p>Dans ce cas, les données sont traitées conformément aux dispositions de la loi du 8 décembre 1992 Relative à la
    protection de la vie privée et au traitement des données à caractère personnel.</p>
<p>Concrètement, cela signifie notamment que :</p>
<ul>
    <li>les données à caractère personnel ne peuvent être recueillies et traitées, comme but de ce mettre en contact
        avec vous et vous identifier, de répondre à la demande d'information que vous avez introduite, ou de réaliser le
        service de votre choix on line&nbsp;;
    </li>
    <li>Le traitement des données personnelles reste limité jusqu’au&nbsp; but visé;</li>
    <li>les données personnelles ne sont gardé plus longtemps que nécessaire jusqu’au&nbsp; but visé</li>
    <li>vos données personnelles ne sont pas communiqués au tiers ni au autres usage de direct-marketing ;</li>
    <li>vous avez toujours accès à vos données personnelles et vous savez vérifier la justesse.</li>
    <li>Vous avez la possibilité de faire corriger les erreurs de vos données personnelles. Vous pouvez contacter le
        gestionnaire de votre site web;
    </li>
    <li>En outre, vous pouvez à tout moment demander que vos données personnelles soient effacées de la police.
    </li>
</ul>
<p>Nous essayons d’atteindre les meilleurs mesures de sécurité pour éviter que des tiers faces mauvaises usages de vos
    données personnelles. Le site web fait usage des dit ’cookies’. Se sont des petits fichiers d’information qui sont
    sauvés automatiquement sur le disque dure de votre ordinateur. Cette information est a chaque visite renvoyer vers
    le gestionnaire de votre site web. Le code qui est traité dans votre cookie permet de reconnaitre votre PC à la
    prochaine visite. Ceci simplifie l’accès au site web et la navigation sera plus vite et efficient. Vous avez la
    possibilité de configurer votre browser de telle façon que les cookies soient empêchés ou pas.</p>